public class Question2 {
    //Question2:Difference between Abstract class and Interface

    //1、Type of method.
    //   Abstract class have abstract and non-abstract methods.
    //   Interface only have abstract methods.
    //2、Type of variable.
    //   Abstract class have final, non-final, static, non-static variables.
    //   Interface only have final  and static variables.
    //3、Implementation.
    //   Abstract class could provide the implementation of the interface.
    //   Interface could not provide the implementation of abstract class.
    //4、Multiple implementation.
    //   Abstract class can extend another class and implement multiple Java interface.
    //   Interface only can extend another interface.
    //5、Accessibility of Data.
    //   Abstract class can have class member.
    //   Interface are public by default.
    //6、Final variable
    //   Abstract class can contain non-final variable.
    //   Interface only declares variable by default final.

}
